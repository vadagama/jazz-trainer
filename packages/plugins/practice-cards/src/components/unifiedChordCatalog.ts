/**
 * Каталог ступеней для источника «Единый» в конфигурации аккордового упражнения.
 *
 * Группировка — по качеству/функции аккорда. У каждой группы два набора:
 * - `common` — курируемый частый набор (показывается по умолчанию);
 * - `full` — полный перечень ступеней этого качества (по кнопке «полный каталог»).
 *
 * Символы записаны в нотации degree-DSL (см. docs/ALL_CHORDS.md) и валидны для
 * `parseDegree`. Ступени — только диатонические (I…VII); хроматические
 * альтерации задаются выбором тональности, а не префиксом ступени.
 */

/**
 * Диатонические ступени (без альтераций префиксом ♭/♯).
 * Альтерации задаются выбором тональности, а не ступенью, поэтому
 * хроматические степени (♭II, ♯IV, ♭VI, …) в каталоге не показываем.
 */
const ALL_DEGREES = ['I', 'II', 'III', 'IV', 'V', 'VI', 'VII'] as const;

/** degree + суффикс качества → массив символов ступеней. */
function q(suffix: string, degrees: readonly string[] = ALL_DEGREES): string[] {
  return degrees.map((d) => d + suffix);
}

export interface ChordGroup {
  /** Заголовок группы. */
  label: string;
  /** Частый набор — показывается по умолчанию. */
  common: string[];
  /** Полный набор для этой группы — показывается в режиме «полный каталог». */
  full: string[];
}

